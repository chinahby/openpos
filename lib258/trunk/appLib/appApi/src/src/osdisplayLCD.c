static int common_readGB18030Dot16(unsigned char ucQM,unsigned char ucWM,unsigned char *pucPtr)
{
	int fd_zk; 
	off_t currpos;
	unsigned int uiOffset;
	unsigned char aucDotBuf[32];
	
	if((ucQM < 0x80)||(ucWM < 0x40))
		return OSDISPLAY_OK;
	
	if((ucQM >= 0xB0)&&(ucWM >= 0xA1))
	{
		ucQM = ucQM - 0xB0;
		ucWM = ucWM - 0xA1;
		uiOffset = ucQM * 94 + ucWM;
	}
	else if(ucQM <= 0xA0)
	{
		ucQM = ucQM - 0x81;
		ucWM = ucWM - 0x40;
		uiOffset = ucQM * 190 + ucWM + 6768;
		if(ucWM > 0x3E)
			uiOffset --;
	}
	else if((ucQM >= 0xAA)&&(ucWM <= 0xA0))
	{
		ucQM = ucQM - 0xAA;
		ucWM = ucWM - 0x40;
		uiOffset = ucQM * 96 + ucWM + 12847;
	}
	
	uiOffset *= 32;
	
	fd_zk = open("/fonts/jf3s1516.hz",O_RDONLY);
	if(fd_zk<0)
	{
		return OSDISPLAY_OPENFONTERR;
	}
	
	currpos = lseek(fd_zk,uiOffset,SEEK_SET);
	if(currpos <0)
		return OSDISPLAY_OPENFONTERR;
	if(read(fd_zk,aucDotBuf,32)<0)
		return OSDISPLAY_OPENFONTERR;
	if( pucPtr )
		memcpy(pucPtr,aucDotBuf,32);
	
	return OSDISPLAY_OK;
}

static int Os__display_6X8(unsigned char line,unsigned char column,char *Text)
{
    struct display_struct display_p;
    unsigned char len;	
    int lcd_fd;	
	
    lcd_fd = open("/dev/lcd",O_RDWR);	
    if(lcd_fd<0)
    {
		return OSDISPLAY_OPENDEVICEERR;
    }

    len = strlen(Text);

    if((line>8)||(column>21))
		return OSDISPLAY_DISPLAYBEYOND;
    display_p.line = line;
    display_p.column = column;
    display_p.pText = Text;
    display_p.text_len = len;

    ioctl(lcd_fd,LCD_DISPLAY_8X6,&display_p);

    close(lcd_fd);
    return OSDISPLAY_OK;
}

