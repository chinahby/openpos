static  int get_cnstr(char *dest, char *src, size_t n)
{
  
  unsigned char bytes;
  unsigned long offset;  
  int qm,wm;
  int i,j;  
  int fd;

  if(src == 0)
  {
     return -1;
  }
  
  fd = open(Font_Data, O_RDONLY);
  if(fd < 0)
  {
    return OSDISPLAY_OPENFONTERR;
  }
        memset(dest, 0, n);
        
  qm = *src;
  wm = *(src+1);
  
  
  if ((qm < 0x80)||(wm < 0x40)) 
  {
    return OSDISPLAY_OK;
  }
  
  if ((qm >= 0xB0)&&(wm >= 0xA1))
  {
      qm = qm - 0xB0;
      wm = wm - 0xA1;
      offset = qm * 94 + wm;
  }
  else if (qm <= 0xA0)
  {
      qm = qm - 0x81;
      wm = wm - 0x40;
      offset = qm * 190 + wm + 6768;
      if (wm > 0x3E)
      {
        offset --;
      }
  }
  else if ((qm >= 0xAA)&&(wm <= 0xA0))
  {
      qm = qm - 0xAA;
      wm = wm - 0x40;
      offset = qm * 96 + wm + 12848;    //xu.xb
      if (wm > 0x3E)
      {
        offset --;
      }        
  } 
  else if ((qm >= 0xA1)&&(wm >= 0xA1))
  {
      qm = qm - 0xA1;
      wm = wm - 0xA1;
      offset = qm * 94 + wm + 12848 + 8160;      
  }   
  else if ((qm >= 0xA8)&&(wm <= 0x96))
  {
      qm = qm - 0xA8;
      wm = wm - 0x40;
      offset = qm * 87 + wm + 12848 + 8160 + 846;      
  }     
  
  offset *= ((FONT_HEIGHT * FONT_WIDTH) >> 3);
  
  lseek(fd,offset,SEEK_SET);
  read(fd, dest, ((FONT_HEIGHT * FONT_WIDTH) >> 3));
  
  close(fd);
  
  return OSDISPLAY_OK;

}

