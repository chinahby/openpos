void OSMMI_clr_Display(unsigned char ucFont,unsigned char line)
{
	int lcd_fd;	
	
	lcd_fd = open("/dev/lcd",O_RDWR);	
	if(lcd_fd<0)
	{
		return;
	}	
	
	if((line >8)&&(line!=0xFF))
	{
		close(lcd_fd);
		return;
	}
	
	switch(ucFont)
	{
		case 0x31:
			ioctl(lcd_fd,LCD_CLR_DISPLAY,&line);
			break; 
		case 0x30:
			ioctl(lcd_fd,LCD_CLR_ONE_LINE,&line);
			break;
		default:
			close(lcd_fd);
			return;
	}
	
	close(lcd_fd);
}

